x = 4
y = 3
print("\nArithmetic Operators: \n")
print('x = ', x, 'y = ', y)
print('-------------------------')
print('Addition of \'x\' + \'y\': ', x + y)
print('Subtraction of \'x\' - \'y\': ', x - y)
print('Multiplication of \'x\' * \'y\': ', x * y)
print('Float Division of \'x\' / \'y\': ', x / y)
print('Integer Division of \'x\' // \'y\': ', x // y)
print('Exponential of \'x\' ** \'y\': ', x ** y)
print('Modulus of \'x\' % \'y\': ', x % y)

x = True
y = False
print("\nLogical/Boolean Operators: \n")
print('x = ', x, 'y = ', y)
print('-------------------------')
print('x and y: ', x and y)
print('x or y: ', x or y)
print('not y: ', not y)

x = 5
y = 10
print("\nComparison Operators: \n")
print('x = ', x, 'y = ', y)
print('-------------------------')
print('x == y: ', x == y)
print('x < y: ', x < y)
print('x > y: ', x > y)
print('x <= y: ', x <= y)
print('x >= y: ', x >= y)
print('x != y: ', x != y)


